// すでに出したお題(used)を避けてランダムに1問返す。
// 全部出切ったら（プールが空になったら）リセットして、直前の1問だけ避ける。
function pickTopic(used = []) {
  const usedSet = new Set(used);
  let pool = TOPICS.filter((t) => !usedSet.has(t));
  if (pool.length === 0) {
    const last = used[used.length - 1];
    pool = TOPICS.filter((t) => t !== last);
    if (pool.length === 0) pool = TOPICS.slice();
  }
  return pool[Math.floor(Math.random() * pool.length)];
}

